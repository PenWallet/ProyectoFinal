<?php

namespace App\Http\Controllers;

use App\User;
use Illuminate\Http\Request;

class UserController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $users = User::all();
        return response()->json($users);
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $user = User::create([
            'nickname' => $request->nickname,
            'password' => bcrypt($request->password),
            'profileDescription' => $request->profileDescription,
            'profileLocation' => $request->profileLocation,
            'birthDate' => $request->birthDate,
            'privateProfile' => $request->privateProfile,
            'accountCreationDate' => $request->accountCreationDate,
            'admin' => $request->admin,
            'bannedUntil' => $request->bannedUntil,
            'banReason' => $request->banReason,
            'online' => $request->online,
        ]);

        $token = auth()->login($user);

        return $this->respondWithToken($token);
    }

    /**
     * Display the specified resource.
     *
     * @param  \App\User  $user
     * @return \Illuminate\Http\Response
     */
    public function show(User $user)
    {
        echo $user;
        $credentials = $user->only(['nickname', 'password']);

        if (!$token = auth()->attempt($credentials)) {
            return response()->json(['error' => 'Unauthorized'], 401);
        }

        return $this->respondWithToken($token);
    }


